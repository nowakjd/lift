    private void ButtonStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonStartActionPerformed
        
        Pietro pom;
        Zmienne.lista.lista.clear();
        koloruj();
        timer =new javax.swing.Timer(czas,new ActionListener()
        {
          int i =0;         
          int j =0;
          int k =0;
          int pierwszeaktualne=0;
          int pierwszepoprzednie=0;
          int drugieaktualne=0;
          int drugiepoprzednie=0;
          String pierwsze="";
          String drugie="";
          String calosc="";
          String wejsciejeden="";
          String wejsciedwa="";
          String wyjsciejeden="";
          String wyjsciedwa="";
          Pietro pom;
          Boolean zmianajedenplus;
          Boolean zmianadwaplus;
          Boolean zmianajedenminus;
          Boolean zmianadwaminus;
          Boolean zmianajedenoba;
          Boolean zmianadwaoba;

            public void actionPerformed(ActionEvent evt)
            {
                pierwsze="";
                drugie="";
                wejsciejeden="";
                wejsciedwa="";
                wyjsciejeden="";
                wyjsciedwa="";
                zmianajedenplus = false;
                zmianadwaplus = false;
                zmianadwaminus = false;
                zmianajedenminus = false;
                zmianajedenoba = false;
                zmianadwaoba = false;
                if (k<Zmienne.listapieterjeden.size()){
                    pierwszepoprzednie = pierwszeaktualne;
                    pierwszeaktualne = Zmienne.listapieterjeden.get(k).numerPietra;
                    if (Zmienne.listapieterjeden.get(k).iloscWchodzacych!=0)
                    {
                        zmianajedenplus = true;
                        wejsciejeden=String.valueOf(Zmienne.listapieterjeden.get(k).iloscWchodzacych);
                    }
                    if (Zmienne.listapieterjeden.get(k).iloscWychodzacych!=0)
                    {
                        if (zmianajedenplus)
                            zmianajedenoba = true;
                        zmianajedenminus = true;
                        wyjsciejeden=String.valueOf(Zmienne.listapieterjeden.get(k).iloscWychodzacych);
                    }
                    szyb1.get(pierwszeaktualne).setBackground(Color.red);
                    if (zmianajedenoba)
                        szyb1.get(pierwszeaktualne).setText("+" + wejsciejeden + " -" +wyjsciejeden);
                    if (zmianajedenplus)
                        szyb1.get(pierwszeaktualne).setText("+" + wejsciejeden);
                    if (zmianajedenminus)
                        szyb1.get(pierwszeaktualne).setText("-" + wyjsciejeden);
                    if (pierwszeaktualne!=pierwszepoprzednie)
                        szyb1.get(pierwszepoprzednie).setBackground(Color.white); 
                    szyb1.get(pierwszepoprzednie).setText("");
                    pierwsze = Zmienne.listapieterjeden.get(pierwszeaktualne).toString();
              
                }
                if (j<Zmienne.listapieterdwa.size()){
                    drugiepoprzednie = drugieaktualne;
                    drugieaktualne = Zmienne.listapieterdwa.get(j).numerPietra;

                    if (Zmienne.listapieterdwa.get(j).iloscWchodzacych!=0)
                    {
                        zmianadwaplus = true;
                        wejsciedwa=String.valueOf(Zmienne.listapieterdwa.get(j).iloscWchodzacych);
                    }
                    if (Zmienne.listapieterdwa.get(j).iloscWychodzacych!=0)
                    {
                        if (zmianadwaplus)
                            zmianadwaoba = true;
                        zmianadwaminus = true;
                        wyjsciedwa=String.valueOf(Zmienne.listapieterdwa.get(j).iloscWychodzacych);
                    }
                    szyb2.get(drugieaktualne).setBackground(Color.red);
                    if (zmianadwaoba)
                        szyb2.get(drugieaktualne).setText("+" + wejsciedwa + " -" +wyjsciedwa);
                    if (zmianadwaplus)
                        szyb2.get(drugieaktualne).setText("+" + wejsciedwa);
                    if (zmianadwaminus)
                        szyb2.get(drugieaktualne).setText("-" + wyjsciedwa);

                    
                   
                    if (drugieaktualne != drugiepoprzednie)
                    szyb2.get(drugiepoprzednie).setBackground(Color.white);
                    szyb2.get(drugiepoprzednie).setText("");
                    drugie = Zmienne.listapieterdwa.get(j).toString();
                }
                calosc +=pierwsze;
                calosc += drugie;
                TextAreaStanWind.setText(calosc);
                j++;
                k++;
                i++;
                if (i == Zmienne.lista.lista.size())
                    timer.stop();
            }
        });
        if (Zmienne.listaPasazerow.isEmpty())
            TextAreaStanWind.setText("Nie podano pasażerów");
        else
        {
            LiftApp.startuj();


        for (int j = 0; j<Zmienne.lista.lista.size();j++)
            if ((pom = Zmienne.lista.lista.get(j)).numerWindy==1)
                Zmienne.listapieterjeden.add(pom);
                else Zmienne.listapieterdwa.add(pom);

        timer.start();
        ButtonStop.setEnabled(true);
        }
    }//GEN-LAST:event_ButtonStartActionPerformed

