    private void SliderIloscPieterStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_SliderIloscPieterStateChanged
        JSlider source = (JSlider)evt.getSource();
        if (!source.getValueIsAdjusting()){
            lift.Zmienne.iloscPieter=source.getValue();

            for (JTextField pieterko : szyb1) {
                pieterko.setVisible(true);
            }
            if (Zmienne.iloscWind == 2)
                for (JTextField pieterko : szyb2) {
                    pieterko.setVisible(true);
                }

                for (int i = 20; i>(source.getValue()); i--) {
                    szyb1.get(i).setVisible(false);
                    szyb2.get(i).setVisible(false);
                }
        }
}//GEN-LAST:event_SliderIloscPieterStateChanged
